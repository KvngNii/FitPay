'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'

const navItems = [
  { href: '/trainer/dashboard', label: 'Home', icon: HomeIcon },
  { href: '/trainer/clients', label: 'Clients', icon: UsersIcon },
  { href: '/trainer/sessions', label: 'Sessions', icon: CalendarIcon },
  { href: '/trainer/log', label: 'Log', icon: ClipboardIcon },
  { href: '/trainer/earnings', label: 'Earnings', icon: CashIcon },
]

export default function TrainerLayout({ children }: { children: React.ReactNode }) {
  const pathname = usePathname()

  return (
    <div className="min-h-screen flex flex-col">
      <div className="flex-1 pb-20">{children}</div>

      <nav className="fixed bottom-0 left-0 right-0 bg-slate-900 border-t border-slate-800 z-50">
        <div className="max-w-2xl mx-auto flex items-center justify-around h-16 px-2">
          {navItems.map(({ href, label, icon: Icon }) => {
            const active = pathname === href
            return (
              <Link
                key={href}
                href={href}
                className={`flex flex-col items-center gap-1 px-3 py-1 rounded-lg transition-colors ${
                  active ? 'text-emerald-400' : 'text-slate-500 hover:text-slate-300'
                }`}
              >
                <Icon className="w-5 h-5" />
                <span className="text-xs font-medium">{label}</span>
              </Link>
            )
          })}
        </div>
      </nav>
    </div>
  )
}

